using FluentValidation;
using MediatR;

namespace LCPC.Domain.Validates;

public class IValidateBeforeRequest<TRequest, TResponse> : IPipelineBehavior<TRequest, TResponse>
    where TRequest : IRequest<TResponse>
{
    private readonly IEnumerable<IValidator<TRequest>> _validators;

    public IValidateBeforeRequest(IEnumerable<IValidator<TRequest>> validators)
    {
        _validators = validators;
    }

    public async Task<TResponse> Handle(TRequest request, RequestHandlerDelegate<TResponse> next,
        CancellationToken cancellationToken)
    {
        var failures = _validators
            .Select(v => v.Validate(request))
            .SelectMany(result => result.Errors)
            .Where(error => error != null)
            .ToList();

        if (failures.Any())
        {
            string message = string.Join(",", failures.Select(x => x.ErrorMessage));
            throw new Exception(message);
        }

        return await next();
    }
}